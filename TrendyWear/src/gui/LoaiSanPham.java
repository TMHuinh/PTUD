    public void dataToTable() {
        List<entity.LoaiSanPham> list = lsp_d.layDSLSP();
        for (entity.LoaiSanPham lsp : list) {
            dtm.addRow(new Object[]{
                lsp.getMaLoai(),
                lsp.getTenLoai(),
                lsp.getMoTa()
            });
        }
    }

    public void removedataTable() {
        dtm.setRowCount(0);

    }

    public boolean check() {
        if (!jTextField1.getText().trim().matches("^L\\d+$")) {
            JOptionPane.showMessageDialog(this, "Mã không đúng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (jTextField2.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Tên không đúng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (jTextField3.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Mô tả không đúng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

