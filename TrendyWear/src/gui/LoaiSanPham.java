    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int r = jTable2.getSelectedRow();
        if (r != -1) {
            int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa loại sản phẩm này?",
                    "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                if (lsp_d.xoaLSP(dtm.getValueAt(r, 0).toString())) {
                    removedataTable();
                    dataToTable();
                    xt();
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                }else{
                    JOptionPane.showMessageDialog(this,"Loại sản phẩm đã có sản phẩm trong hệ thống","Lỗi",JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Hãy chọn dòng cần xóa");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = jTable2.getSelectedRow();
        if (row != -1) {
            if (check()) {
                entity.LoaiSanPham lsp = new entity.LoaiSanPham(jTextField1.getText(), jTextField2.getText(), jTextField3.getText());
                if (lsp_d.CapNhat(lsp)) {
                    JOptionPane.showMessageDialog(this, "Cập nhật thành công", "Thông báo",
                            JOptionPane.INFORMATION_MESSAGE);
                    xt();
                    removedataTable();
                    dataToTable();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một khách hàng", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        int r = jTable2.getSelectedRow();
        int mouseCount = evt.getButton();
        if (r != -1) {
            jTextField1.setText(dtm.getValueAt(r, 0).toString());
            jTextField1.setEditable(false);
            jTextField2.setText(dtm.getValueAt(r, 1).toString());
            jTextField3.setText(dtm.getValueAt(r, 2).toString());
        }
        if (mouseCount > 1) {
            jTable2.clearSelection();
            jTextField1.setEditable(true);
            xt();
        }
    }//GEN-LAST:event_jTable2MouseClicked

