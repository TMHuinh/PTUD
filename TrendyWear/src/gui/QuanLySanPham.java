    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        int r = jTable2.getSelectedRow();
        int mouseCount = evt.getButton();
        if (r != -1) {
            jtf_ma.setText(dtm.getValueAt(r, 0).toString());
            jtf_ma.setEditable(false);
            jtf_ten.setText(dtm.getValueAt(r, 1).toString());
            jtf_th.setText(dtm.getValueAt(r, 2).toString());
            jcbb_ncc.setSelectedItem(dtm.getValueAt(r, 3));
            String value = dtm.getValueAt(r, 4).toString();
            jcbb_kc.setSelectedItem(value.trim());
            jtf_slt.setText(dtm.getValueAt(r, 5).toString());
            jtf_gia.setText(dtm.getValueAt(r, 6).toString());
            jtf_ms.setText(dtm.getValueAt(r, 7).toString());
            jcbb_lsp.setSelectedItem(dtm.getValueAt(r, 8));
            img = sp_d.getURL(dtm.getValueAt(r, 0).toString());
            jLabel11.setIcon(new ImageIcon(img));
        }
        if (mouseCount > 1) {
            jTable2.clearSelection();
            jtf_ma.setEditable(true);
            jLabel11.setIcon(new ImageIcon(""));
            xt();
        }
    }//GEN-LAST:event_jTable2MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int r = jTable2.getSelectedRow();
        if (r != -1) {
            int chon = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa sản phẩm này", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
            if (chon == JOptionPane.YES_OPTION) {
                SanPham_Dao spd = new SanPham_Dao();
                if (spd.xoaSP(dtm.getValueAt(r, 0).toString())) {
                    removedataTable();
                    dataToTable();
                    JOptionPane.showMessageDialog(this, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    xt();
                }else{
                    JOptionPane.showMessageDialog(this,"Sản phẩm này đã có hóa đơn","Lỗi",JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm cần xóa", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int r = jTable2.getSelectedRow();
        if (r != -1) {
            if (check()) {
                SanPham_Dao spd = new SanPham_Dao();
                SanPham sp = new SanPham(jtf_ma.getText(),
                        jtf_ten.getText(),
                        jtf_ms.getText(),
                        jcbb_kc.getSelectedItem().toString(),
                        img,
                        jtf_th.getText(),
                        Double.parseDouble(jtf_gia.getText()),
                        Integer.parseInt(jtf_slt.getText()),
                        new NhaCungCap(mapNCC.get(jcbb_ncc.getSelectedItem().toString())),
                        new entity.LoaiSanPham(mapLSP.get(jcbb_lsp.getSelectedItem().toString())));
                if (spd.CapNhat(sp)) {
                    JOptionPane.showMessageDialog(this, "Cập nhật thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    xt();
                    removedataTable();
                    dataToTable();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một sản phẩm", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

