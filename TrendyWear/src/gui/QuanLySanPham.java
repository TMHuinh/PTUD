    public void dataToTable() {
        List<SanPham> list = sp_d.layDSSP();
        for (SanPham sp : list) {
            dtm.addRow(new Object[]{
                sp.getMaSP(),
                sp.getTenSP(),
                sp.getThuongHieu(),
                sp.getNhaCungCap().getTenNCC(),
                sp.getKichCo(),
                sp.getSoLuongTon(),
                sp.getGia(),
                sp.getMauSac(),
                sp.getLoaiSanPham().getTenLoai()
            });
        }
    }

    public void dataNCC() {
        List<NhaCungCap> list = ncc_d.layDSNCC();
        for (NhaCungCap ncc : list) {
            jcbb_ncc.addItem(ncc.getTenNCC());
            mapNCC.put(ncc.getTenNCC(), ncc.getMaNCC());
        }
    }

    public void dataLSP() {
        List<entity.LoaiSanPham> list = lsp_d.layDSLSP();
        for (entity.LoaiSanPham lsp : list) {
            jcbb_lsp.addItem(lsp.getTenLoai());
            mapLSP.put(lsp.getTenLoai(), lsp.getMaLoai());
        }
    }

    public boolean check() {
        if (!jtf_ma.getText().trim().matches("^SP\\d+$")) {
            JOptionPane.showMessageDialog(this, "Mã không đúng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (jtf_ten.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Tên không đúng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (jtf_th.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Thương hiệu không đúng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (!jtf_slt.getText().trim().matches("^\\d+$")) {
            JOptionPane.showMessageDialog(this, "Số lượng tồn không đúng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (jtf_gia.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Giá không đúng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (jtf_ms.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Màu sắc không đúng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (img.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn hình ảnh", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

